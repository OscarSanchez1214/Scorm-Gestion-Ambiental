		function dragAndDrop(dragName, dropName, containerDragAndDrop, childrens) {
		
			let drag;
			let drop = containerDragAndDrop[dropName];
			let containerDrag = [null];
		
			stage.enableTouch = true;
			stage.mouseMoveOutside = true;
		
			let positionCollisionX = drop.x;
			let positionCollisionY = drop.y;
		
			let dropWidth = drop.nominalBounds.width / 2;
			let dropHeight = drop.nominalBounds.height / 2;
		
		
			let limitLeftX = positionCollisionX - dropWidth;
			let limitRightX = positionCollisionX + dropWidth;
		
			let limitTopY = positionCollisionY - dropHeight;
			let limitBottomY = positionCollisionY + dropHeight;
		
		
			for (let i = 1; i <= childrens; i++) {
		
				drag = containerDragAndDrop[dragName + i];
				drag.id = i;
				drag.initialPositionX = drag.x;
				drag.initialPositionY = drag.y;
				drag.mouseChildren = false;
		
				containerDrag.push(drag);
		
				containerDrag[i].addEventListener("mousedown", dragMouseDown);
		
				function dragMouseDown(e) {
		
					let offset = {
						x: stage.x - e.stageX,
						y: stage.y - e.stageY
					};
		
		
					containerDrag[i].addEventListener("pressmove", dragPressMove);
		
					function dragPressMove(evt) {
		
		
						if (e.currentTarget.id == 2) {
		
							e.currentTarget.x = (evt.stageX + offset.x) / 2 - (150);
							e.currentTarget.y = (evt.stageY + offset.y) / 2 + (150);
		
						} else if (e.currentTarget.id == 1) {
		
							e.currentTarget.x = (evt.stageX + offset.x) / 2 - (300);
							e.currentTarget.y = (evt.stageY + offset.y) / 2 + (100);
						}
		
		
		
		
						if ((e.currentTarget.x >= limitLeftX && e.currentTarget.x <= limitRightX) && (e.currentTarget.y >= limitTopY && e.currentTarget.y <= limitBottomY)) {
		
							drop.gotoAndStop(e.currentTarget.id);
							e.currentTarget.visible = false;
							checkDragF(e.currentTarget.id);
							
						}
		
		
					}
					stage.addEventListener("pressup", stagePressUp);
		
					function stagePressUp(evt) {
						e.currentTarget.x = e.currentTarget.initialPositionX;
						e.currentTarget.y = e.currentTarget.initialPositionY;
		
						for (let ii = 1; ii <= childrens; ii++) {
							if (e.currentTarget.id != ii) {
								if (drop.currentFrame == ii) {
									e.currentTarget.visible = true;
								
								}
							}
						}
					}
		
				}
		
			}
		
		}
		
